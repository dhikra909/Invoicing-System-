package syain;

public class Statistics {

	private Integer No_Of_Items;
	private Integer No_of_Invoices;
	private Integer Total_Sales;

	public Integer getNo_Of_Items() {
		return No_Of_Items;
	}

	public void setNo_Of_Items(Integer no_Of_Items) {
		No_Of_Items = no_Of_Items;
	}

	public Integer getNo_of_Invoices() {
		return No_of_Invoices;
	}

	public void setNo_of_Invoices(Integer no_of_Invoices) {
		No_of_Invoices = no_of_Invoices;
	}

	public Integer getTotal_Sales() {
		return Total_Sales;
	}

	public void setTotal_Sales(Integer total_Sales) {
		Total_Sales = total_Sales;
	}

}
